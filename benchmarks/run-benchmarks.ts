#!/usr/bin/env tsx
/**
 * Token Reduction Benchmark Runner
 *
 * For each of the 10 predefined workflow definitions in the registry:
 * 1. Extracts only the endpoints used by that workflow
 * 2. Runs MinimalityAnalyzer to get token metrics
 * 3. Records: workflow name, step count, tool count, Zod schema tokens,
 *    total context tokens, and reduction percentage vs full RC API
 * 4. Writes results to benchmarks/BENCHMARKS.md
 *
 * Usage: npm run benchmark
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { SchemaExtractor } from "../src/core/schema-extractor.js";
import { MinimalityAnalyzer } from "../src/core/minimality-analyzer.js";
import { WorkflowRegistry } from "../src/core/workflow-registry.js";
import { WorkflowComposer } from "../src/core/workflow-composer.js";
import { VALID_DOMAINS, type Domain } from "../src/core/types.js";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const OPENAPI_SPEC_DIR = resolve(
  __dirname,
  "../src/providers/rocketchat/openapi",
);
const OUTPUT_PATH = resolve(__dirname, "BENCHMARKS.md");

interface BenchmarkRow {
  workflowName: string;
  stepCount: number;
  toolCount: number;  // always 1 for a workflow (composite)
  zodSchemaTokens: number;
  totalContextTokens: number;
  fullServerTokens: number;
  reductionPercentage: number;
}

async function main() {
  console.log("═══ Token Reduction Benchmark ═══\n");

  // 1. Load ALL domains to get the full RC API baseline
  console.log("Loading all RC API domains...");
  const extractor = new SchemaExtractor();
  await extractor.loadDomains([...VALID_DOMAINS] as Domain[]);

  const totalEndpoints = extractor.getEndpointCount();
  console.log(`  Total RC API endpoints: ${totalEndpoints}`);

  const analyzer = new MinimalityAnalyzer();
  const registry = new WorkflowRegistry();
  const composer = new WorkflowComposer();
  const workflows = registry.listWorkflows();

  // 2. Get full-server baseline — use ALL endpoints for an accurate baseline
  const allEndpoints = extractor.getAllEndpoints();
  const baselineReport = analyzer.analyze(OPENAPI_SPEC_DIR, allEndpoints, []);
  const fullServerTokens = baselineReport.tokens.fullServerTokens;
  console.log(`  Full server token estimate: ~${fullServerTokens.toLocaleString()}\n`);

  // 3. Benchmark each workflow
  const rows: BenchmarkRow[] = [];

  for (const def of workflows) {
    const opIds = def.steps.flatMap((s) => s.fallbackOperationId ? [s.operationId, s.fallbackOperationId] : [s.operationId]);

    try {
      // Extract only the endpoints needed by this workflow
      const workflowEndpoints = extractor.extractEndpointsForIds(opIds);

      // Generate the composite tool to measure Zod schema weight
      const composedTool = composer.compose(def, workflowEndpoints);
      const zodTokens = Math.ceil(composedTool.zodSchemaCode.length / 4); // ~4 chars per token

      // Run minimality analysis
      const report = analyzer.analyze(
        OPENAPI_SPEC_DIR,
        workflowEndpoints,
        [],
      );

      const row: BenchmarkRow = {
        workflowName: def.name,
        stepCount: def.steps.length,
        toolCount: 1, // composite = one tool
        zodSchemaTokens: zodTokens,
        totalContextTokens: report.tokens.minimalServerTokens,
        fullServerTokens: report.tokens.fullServerTokens,
        reductionPercentage: report.tokens.tokenReductionPercentage,
      };

      rows.push(row);
      console.log(
        `  ✓ ${def.name}: ${row.stepCount} steps → 1 tool, ~${row.totalContextTokens} tokens (${row.reductionPercentage.toFixed(1)}% reduction)`,
      );
    } catch (err) {
      console.log(
        `  ✗ ${def.name}: ${err instanceof Error ? err.message : String(err)}`,
      );
    }
  }

  // 4. Generate BENCHMARKS.md
  const md = generateMarkdown(rows, totalEndpoints, fullServerTokens, workflows.length);
  writeFileSync(OUTPUT_PATH, md, "utf-8");
  console.log(`\n✓ Benchmark report written to ${OUTPUT_PATH}`);
}

function generateMarkdown(
  rows: BenchmarkRow[],
  totalEndpoints: number,
  fullServerTokens: number,
  totalWorkflows: number,
): string {
  const now = new Date().toISOString().split("T")[0];

  let md = `# Token Reduction Benchmarks\n\n`;
  md += `> Auto-generated by \`npm run benchmark\` on ${now}\n\n`;

  md += `## Baseline\n\n`;
  md += `| Metric | Value |\n`;
  md += `|--------|-------|\n`;
  md += `| Total RC API endpoints | ${totalEndpoints} |\n`;
  md += `| Full server estimated tokens | ~${fullServerTokens.toLocaleString()} |\n\n`;

  md += `## Workflow Benchmarks\n\n`;
  md += `Each workflow composes multiple raw API endpoints into a **single composite MCP tool**.\n`;
  md += `Token counts measure the context footprint of only the endpoints needed by that workflow.\n\n`;

  md += `| Workflow | Steps | Tools | Zod Tokens | Context Tokens | Full Server | Reduction |\n`;
  md += `|----------|-------|-------|------------|----------------|-------------|----------|\n`;

  for (const row of rows) {
    md += `| \`${row.workflowName}\` | ${row.stepCount} | ${row.toolCount} | ~${row.zodSchemaTokens} | ~${row.totalContextTokens.toLocaleString()} | ~${row.fullServerTokens.toLocaleString()} | **${row.reductionPercentage.toFixed(1)}%** |\n`;
  }

  md += `\n`;

  // Summary stats
  if (rows.length > 0) {
    const avgReduction =
      rows.reduce((sum, r) => sum + r.reductionPercentage, 0) / rows.length;
    const avgContextTokens =
      rows.reduce((sum, r) => sum + r.totalContextTokens, 0) / rows.length;

    md += `## Summary\n\n`;
    md += `- **Average token reduction:** ${avgReduction.toFixed(1)}%\n`;
    md += `- **Average context tokens per workflow:** ~${Math.round(avgContextTokens).toLocaleString()}\n`;
    md += `- **Workflows benchmarked:** ${rows.length} / ${totalWorkflows}\n\n`;

    md += `> These results prove that the Minimal MCP Server Generator achieves its primary goal:\n`;
    md += `> **reducing context bloat by ${avgReduction.toFixed(0)}%+** compared to a monolithic server\n`;
    md += `> that exposes all ${totalEndpoints} RC API endpoints.\n`;
  }

  return md;
}

main().catch((err) => {
  console.error("Benchmark failed:", err);
  process.exit(1);
});
